package com.example.codebreaker.List;

public class ItemAdapter {
    private int image_left;
    private int image_center;
    private int image_right;

    public int getImageLeft() {
        return image_left;
    }
    public void setImageLeft(int image) {
        this.image_left = image;
    }
    public int getImageCenter() {
        return image_center;
    }
    public void setImageCenter(int image) {
        this.image_center = image;
    }
    public int getImageRight() {
        return image_right;
    }
    public void setImageRight(int image) {
        this.image_right = image;
    }
}
